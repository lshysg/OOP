class ДенежныйПеревод:
    def __init__(self, сумма, получатель):
        self.сумма = сумма                # публичное поле
        self._получатель = получатель     # защищённое поле
        self.__статус = "Создан"          # приватное поле

    def выполнить(self):
        print(f"Выполняется перевод {self.сумма} руб. получателю {self._получатель}")
        self.__обновить_статус("Выполнен")

    def _проверить_получателя(self):
        return self._получатель is not None

    def __обновить_статус(self, статус):
        self.__статус = статус
        print(f"Статус перевода: {self.__статус}")


class ПочтовыйПеревод(ДенежныйПеревод):
    def __init__(self, сумма, получатель, адрес):
        super().__init__(сумма, получатель)
        self.адрес = адрес

    def выполнить(self):
        if self._проверить_получателя():
            print(f"Отправка почтового перевода на {self.адрес}")
            super().выполнить()


class БанковскийПеревод(ДенежныйПеревод):
    def __init__(self, сумма, получатель, счет):
        super().__init__(сумма, получатель)
        self.__счет_получателя = счет

    def выполнить(self):
        if self._проверить_получателя():
            print(f"Перевод {self.сумма} руб. на счёт {self.__счет_получателя}")
            super().выполнить()

    def _проверить_счет(self):
        return len(str(self.__счет_получателя)) >= 10


class ВалютныйПеревод(БанковскийПеревод):
    def __init__(self, сумма, получатель, счет, валюта):
        super().__init__(сумма, получатель, счет)
        self.валюта = валюта

    def выполнить(self):
        if self._проверить_счет():
            print(f"Выполняется валютный перевод {self.сумма} {self.валюта}")
            super().выполнить()
